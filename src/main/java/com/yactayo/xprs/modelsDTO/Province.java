package com.yactayo.xprs.modelsDTO;

public class Province {
    int idProvince;
    String name;

    public Province() {
    }

    public int getIdProvince() {
        return idProvince;
    }

    public void setIdProvince(int idProvince) {
        this.idProvince = idProvince;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
